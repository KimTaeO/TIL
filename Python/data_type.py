# 정수형 -> 정수를 뜻하는 자료형
a = 100
b = 0
c = -100

# 실수형 -> 소수점이 포함된 자료형
a = 1.2
b = -3.4

# 8진수 -> (0o 또는 0O)를 앞에 붙이고 숫자를 작성
a = 0o1
b = 0O1

# 16진수 -> (0x)를 앞에 붙이고 숫자를 작성
a = 0x1

# 사칙연산은 여느 언어와 같이 "+,-,*,/"를 사용하여 수행한다
# 제곱을 연산할 때에는 "**"를 사용
# 나머지 연산은 "%" 사용, 나눗셈의 몫을 구하려면 "//" 사용

# 파이썬에서 문자열을 표현하는 방법
"Life is too short, You need python" # 첫번째 방법
'Life is too short, You need python' # 두번째 방법
"""Life is too short, You need python""" # 세번째 방법
'''Life is too short, You need python''' # 네번째 방법

# 문자열에 ('),(")를 삽입시키는 방법
# (')를 삽입하려면 문자열을 큰따옴표로 묶으면 된다
# (")는 반대로 작은따옴표로 묶으면 된다
# 둘다 앞에 역슬래쉬를 붙여서도 표현 할 수 있다

